""" Escribir un programa en el cual se ingresen 
    cuatro números, calcular e informar la suma de los 
    dos primeros y el producto del tercero y el cuarto."""


num1 = float(input("Ingrese el primer número: "))
num2 = float(input("Ingrese el segundo número: "))
num3 = float(input("Ingrese el tercer número: "))
num4 = float(input("Ingrese el cuarto número: "))

suma = num1 + num2
producto = num3 * num4

print("La suma de los primeros números es de: " + str(suma))
print("El producto de los ultimos números es de: " + str(producto))

